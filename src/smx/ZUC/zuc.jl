#
# Performance optimizations:
#   1. Ring buffer for LFSR: replaces O(n) push!/popfirst! with O(1) shifts
#   2. Pre-allocated keystream buffer
#   3. zuc_encrypt returns Vector{UInt8} (consistent with SM3/SM4)
#   4. @inbounds on all hot loops
