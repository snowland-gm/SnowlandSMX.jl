# =============================================================================
# ZUC Context  (with ring-buffer LFSR)
# =============================================================================

"""
    ZUCContext

ZUC stream cipher context.

LFSR is implemented as a ring buffer:
- lfsr_buf: 16-element UInt32 array
- lfsr_head: index (1-based) pointing to lfsr[1]

Access: lfsr[i] = lfsr_buf[(head + i - 2) & 15 + 1]
Append:  buf[head] = f; head = (head & 15) + 1

This avoids O(n) popfirst! on every LFSR update.
"""
mutable struct ZUCContext
    lfsr_buf::Vector{UInt32}
    lfsr_head::Int
    r::Vector{UInt32}
    x::Vector{UInt32}

    function ZUCContext(key::Vector{UInt8}, iv::Vector{UInt8})
        ctx = new(zeros(UInt32, 16), 1, zeros(UInt32, 2), zeros(UInt32, 4))
        zuc_init!(ctx, key, iv)
        return ctx
    end
end

# Ring-buffer LFSR helpers
@inline _lfsr_get(ctx::ZUCContext, i::Int) =
    @inbounds ctx.lfsr_buf[((ctx.lfsr_head + i - 2) & 15) + 1]

@inline function _lfsr_set!(ctx::ZUCContext, i::Int, v::UInt32)
    @inbounds ctx.lfsr_buf[((ctx.lfsr_head + i - 2) & 15) + 1] = v
end

@inline function _lfsr_append!(ctx::ZUCContext, f::UInt32)
    @inbounds ctx.lfsr_buf[ctx.lfsr_head] = f
    ctx.lfsr_head = (ctx.lfsr_head & 15) + 1
end

# =============================================================================
# Core ZUC functions
# =============================================================================

@inline function _add_uint31(a::UInt32, b::UInt32)::UInt32
    c = a + b
    return (c & 0x7FFFFFFF) + (c >> 31)
end

@inline function _rotl_uint31(a::UInt32, shift::Integer)::UInt32
    return ((a << shift) | (a >> (31 - shift))) & 0x7FFFFFFF
end

@inline _rotl_uint32(a::UInt32, shift::Integer)::UInt32 = Base.bitrotate(a, shift)

@inline function _l1(x::UInt32)::UInt32
    return x ⊻ _rotl_uint32(x, 2) ⊻ _rotl_uint32(x, 10) ⊻
           _rotl_uint32(x, 18) ⊻ _rotl_uint32(x, 24)
end

@inline function _l2(x::UInt32)::UInt32
    return x ⊻ _rotl_uint32(x, 8) ⊻ _rotl_uint32(x, 14) ⊻
           _rotl_uint32(x, 22) ⊻ _rotl_uint32(x, 30)
end

@inline function _make_uint32(a::Integer, b::Integer, c::Integer, d::Integer)::UInt32
    return (UInt32(a) << 24) | (UInt32(b) << 16) | (UInt32(c) << 8) | UInt32(d)
end

@inline function _make_uint31(a::Integer, b::Integer, c::Integer)::UInt32
    return ((UInt32(a) << 23) | (UInt32(b) << 8) | UInt32(c)) & 0x7FFFFFFF
end

# =============================================================================
# LFSR operations
# =============================================================================

function bit_reorganization!(ctx::ZUCContext)
    @inbounds begin
        ctx.x[1] = ((_lfsr_get(ctx, 16) & 0x7FFF8000) << 1) |
                   (_lfsr_get(ctx, 15) & 0xFFFF)
        ctx.x[2] = ((_lfsr_get(ctx, 12) & 0xFFFF) << 16) |
                   (_lfsr_get(ctx, 10) >> 15)
        ctx.x[3] = ((_lfsr_get(ctx, 8)  & 0xFFFF) << 16) |
                   (_lfsr_get(ctx, 6)  >> 15)
        ctx.x[4] = ((_lfsr_get(ctx, 3)  & 0xFFFF) << 16) |
                   (_lfsr_get(ctx, 1)  >> 15)
    end
end

function lfsr_next(ctx::ZUCContext)::UInt32
    f = _lfsr_get(ctx, 1)
    f = _add_uint31(f, _rotl_uint31(_lfsr_get(ctx, 1), 8))
    f = _add_uint31(f, _rotl_uint31(_lfsr_get(ctx, 5), 20))
    f = _add_uint31(f, _rotl_uint31(_lfsr_get(ctx, 11), 21))
    f = _add_uint31(f, _rotl_uint31(_lfsr_get(ctx, 14), 17))
    f = _add_uint31(f, _rotl_uint31(_lfsr_get(ctx, 16), 15))
    return f
end

function lfsr_init!(ctx::ZUCContext, u::UInt32)
    _lfsr_append!(ctx, _add_uint31(lfsr_next(ctx), u))
end

function lfsr_shift!(ctx::ZUCContext)
    _lfsr_append!(ctx, lfsr_next(ctx))
end

function f!(ctx::ZUCContext)::UInt32
    @inbounds begin
        W  = (ctx.x[1] ⊻ ctx.r[1]) + ctx.r[2]
        W1 = ctx.r[1] + ctx.x[2]
        W2 = ctx.r[2] ⊻ ctx.x[3]
    end
    u = _l1(((W1 & 0x0000FFFF) << 16) | (W2 >> 16))
    v = _l2(((W2 & 0x0000FFFF) << 16) | (W1 >> 16))

    ctx.r[1] = _make_uint32(
        @inbounds(S0[(u >> 24) + 1]),
        @inbounds(S1[((u >> 16) & 0xFF) + 1]),
        @inbounds(S0[((u >> 8) & 0xFF) + 1]),
        @inbounds(S1[(u & 0xFF) + 1])
    )
    ctx.r[2] = _make_uint32(
        @inbounds(S0[(v >> 24) + 1]),
        @inbounds(S1[((v >> 16) & 0xFF) + 1]),
        @inbounds(S0[((v >> 8) & 0xFF) + 1]),
        @inbounds(S1[(v & 0xFF) + 1])
    )
    return W
end

function zuc_init!(ctx::ZUCContext, key::Vector{UInt8}, iv::Vector{UInt8})
    # Load LFSR: si = key[i] || D[i] || iv[i]  (each byte)
    for i in 1:16
        _lfsr_set!(ctx, i, _make_uint31(UInt32(key[i]), UInt32(D[i]), UInt32(iv[i])))
    end
    ctx.r[1] = 0
    ctx.r[2] = 0
    for _ in 1:32
        bit_reorganization!(ctx)
        w = f!(ctx)
        lfsr_init!(ctx, w >> 1)
    end
end

# =============================================================================
# Keystream Generation and Encryption
# =============================================================================

"""
    zuc_generate_keystream(ctx::ZUCContext, length::Integer) -> Vector{UInt32}

Generate ZUC keystream of specified number of 32-bit words.
"""
function zuc_generate_keystream(ctx::ZUCContext, length::Integer)
    bit_reorganization!(ctx)
    f!(ctx)  # Discard the first output
    keystream = Vector{UInt32}(undef, length)
    @inbounds for i in 1:length
        lfsr_shift!(ctx)
        bit_reorganization!(ctx)
        keystream[i] = f!(ctx) ⊻ ctx.x[4]
    end
    lfsr_shift!(ctx)
    return keystream
end

"""
    zuc_encrypt(ctx::ZUCContext, input::Vector{UInt8}) -> Vector{UInt8}

Encrypt/decrypt data using ZUC.  Processes 4 bytes per 32-bit keystream word
(per GM/T 0001-2012 standard).  No intermediate keystream buffer allocation.
"""
function zuc_encrypt(ctx::ZUCContext, input::Vector{UInt8})
    n = length(input)
    output = Vector{UInt8}(undef, n)

    bit_reorganization!(ctx)
    f!(ctx)  # Discard the first output

    n_full = n & ~3  # largest multiple of 4 <= n

    @inbounds for i in 1:4:n_full
        lfsr_shift!(ctx)
        bit_reorganization!(ctx)
        z = f!(ctx) ⊻ ctx.x[4]

        output[i]     = input[i]     ⊻ (z >> 24)
        output[i + 1] = input[i + 1] ⊻ ((z >> 16) & 0xff)
        output[i + 2] = input[i + 2] ⊻ ((z >> 8) & 0xff)
        output[i + 3] = input[i + 3] ⊻ (z & 0xff)
    end

    # Handle remaining 1-3 bytes using next keystream word
    if n_full < n
        lfsr_shift!(ctx)
        bit_reorganization!(ctx)
        z = f!(ctx) ⊻ ctx.x[4]
        for j in 0:(n - n_full - 1)
            output[n_full + j + 1] = input[n_full + j + 1] ⊻ ((z >> (24 - 8 * j)) & 0xff)
        end
    end

    lfsr_shift!(ctx)
    return output
end

